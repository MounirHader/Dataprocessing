// get data
rawdata = document.getElementById('rawdata').value;
var jsondata = JSON.parse(rawdata);

colour_keys = [];
code_list = []
consumption_list = []
beer_list = []
wine_list = []
spirits_list = []
other_list = []
country_list = []

// loop over dataset
for (i = 0, j = jsondata.length; i < j; i++){
  // country of interest
  target_country = jsondata[i][0];
  target_country_consumption = jsondata[i][1];

  // find id and set fillKey
  for (k = 0, l = country_codes.length; k < l; k++){
    if (country_codes[k][2] == target_country){
      land_id = country_codes[k][1];
      code_list.push(land_id);
      consumption_list.push(target_country_consumption)
      beer_list.push(jsondata[i][4])
      wine_list.push(jsondata[i][5])
      spirits_list.push(jsondata[i][6])
      other_list.push(jsondata[i][7])
      country_list.push(target_country)

      if (target_country_consumption > 15 && target_country_consumption <= 18) {
        fillKey = "15+ "
        colour_keys[land_id] = {fillKey};
      }

      else if (target_country_consumption > 10 && target_country_consumption <= 15) {
        fillKey = "10-15 "
        colour_keys[land_id] = {fillKey};
      }

      else if (target_country_consumption > 5 && target_country_consumption <= 10) {
        fillKey = "5-10 "
        colour_keys[land_id] = {fillKey};
      }

      else if (target_country_consumption <= 5){
        fillKey = "0-5 "
        colour_keys[land_id] = {fillKey};
      }
    }}
  }

